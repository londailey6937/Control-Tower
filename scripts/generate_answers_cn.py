import os
from fpdf import FPDF

OUT_DIR = os.path.dirname(os.path.abspath(__file__))
CJK_FONT = "/Library/Fonts/Arial Unicode.ttf"

_MAP = str.maketrans({
    "\u2014": "--", "\u2013": "-", "\u2019": "'", "\u201c": '"', "\u201d": '"',
    "\u2018": "'", "\u2265": ">=", "\u2264": "<=", "\u00b5": "u", "\u00d7": "x",
    "\u2022": "-", "\u2026": "...", "\u00ae": "(R)",
})
def _a(s):
    return s.translate(_MAP)

PRIMARY = (140, 21, 21)
NAVY = (10, 40, 100)
BLUE = (30, 90, 200)
TEXT = (40, 40, 45)
DARK = (25, 25, 30)
GRAY = (120, 120, 130)
GREEN = (34, 139, 34)
LIGHT_BG = (245, 245, 248)


class AnswerPDF(FPDF):

    def __init__(self):
        super().__init__()
        self.add_font("CJK", "", CJK_FONT)
        self.add_font("CJK", "B", CJK_FONT)
        self.add_font("CJK", "I", CJK_FONT)

    def header(self):
        if self.page_no() <= 1:
            return
        self.set_font("CJK", "I", 8)
        self.set_text_color(*GRAY)
        self.cell(0, 5, _a("PMP认证课程  |  练习答案  |  机密"), align="L")
        self.ln(8)

    def footer(self):
        self.set_y(-15)
        self.set_font("CJK", "I", 8)
        self.set_text_color(*GRAY)
        self.cell(0, 10, _a(f"第 {self.page_no()}/{{nb}} 页"), align="C")

    def ex_title(self, module, num, title):
        self.add_page()
        self.set_font("CJK", "B", 10)
        self.set_text_color(*PRIMARY)
        self.cell(0, 6, _a(f"模块 {module}  |  练习 {num}"), new_x="LMARGIN", new_y="NEXT")
        self.set_font("CJK", "B", 15)
        self.set_text_color(*NAVY)
        self.cell(0, 9, _a(title), new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(*PRIMARY)
        self.line(self.l_margin, self.get_y() + 1, self.w - self.r_margin, self.get_y() + 1)
        self.ln(6)

    def prompt(self, text):
        self.set_fill_color(*LIGHT_BG)
        x = self.l_margin
        w = self.w - self.l_margin - self.r_margin
        self.set_font("CJK", "B", 9)
        self.set_text_color(*GRAY)
        self.set_x(x)
        self.cell(w, 5, _a("题目:"), fill=True, new_x="LMARGIN", new_y="NEXT")
        self.set_font("CJK", "I", 9)
        self.set_text_color(*DARK)
        self.set_x(x)
        self.multi_cell(w, 5, _a(text), fill=True, new_x="LMARGIN", new_y="NEXT")
        self.ln(4)

    def answer_head(self):
        self.set_font("CJK", "B", 11)
        self.set_text_color(*GREEN)
        self.cell(0, 7, _a("标准答案"), new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def txt(self, text):
        self.set_font("CJK", "", 10)
        self.set_text_color(*TEXT)
        self.multi_cell(0, 5.5, _a(text), new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def sub(self, title):
        self.set_font("CJK", "B", 10)
        self.set_text_color(*NAVY)
        self.cell(0, 7, _a(title), new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def rubric(self, text):
        self.ln(2)
        self.set_font("CJK", "B", 9)
        self.set_text_color(*PRIMARY)
        self.cell(0, 5, _a("评分标准:"), new_x="LMARGIN", new_y="NEXT")
        self.set_font("CJK", "", 9)
        self.set_text_color(*TEXT)
        self.multi_cell(0, 5, _a(text), new_x="LMARGIN", new_y="NEXT")
        self.ln(2)


