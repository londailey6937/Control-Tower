import os
from fpdf import FPDF

OUT_DIR = os.path.dirname(os.path.abspath(__file__))
CJK_FONT = "/Library/Fonts/Arial Unicode.ttf"

_MAP = str.maketrans({
    "\u2014": "--", "\u2013": "-", "\u2019": "'", "\u201c": '"', "\u201d": '"',
    "\u2018": "'", "\u2265": ">=", "\u2264": "<=", "\u00b5": "u", "\u00d7": "x",
    "\u2022": "-", "\u2026": "...", "\u00ae": "(R)",
})
def _a(s):
    return s.translate(_MAP)

# Color palette
NAVY = (10, 40, 100)
PRIMARY = (140, 21, 21)
BLUE = (30, 90, 200)
DARK = (25, 25, 30)
TEXT = (40, 40, 45)
GRAY = (120, 120, 130)
LIGHT_BG = (245, 245, 248)
WHITE = (255, 255, 255)
GREEN = (34, 139, 34)
AMBER = (200, 150, 0)
RED = (200, 40, 40)


class CoursePDF(FPDF):

    def __init__(self):
        super().__init__()
        self.add_font("CJK", "", CJK_FONT)
        self.add_font("CJK", "B", CJK_FONT)
        self.add_font("CJK", "I", CJK_FONT)

    def header(self):
        if self.page_no() <= 2:
            return
        self.set_font("CJK", "I", 8)
        self.set_text_color(*GRAY)
        self.cell(0, 5, _a("510k Bridge 项目管理认证  |  ICU数字孪生案例研究"), align="L")
        self.ln(8)

    def footer(self):
        self.set_y(-15)
        self.set_font("CJK", "I", 8)
        self.set_text_color(*GRAY)
        self.cell(0, 10, _a(f"第 {self.page_no()}/{{nb}} 页"), align="C")

    def module_title(self, num, title):
        self.add_page()
        self.ln(25)
        self.set_font("CJK", "B", 13)
        self.set_text_color(*PRIMARY)
        self.cell(0, 8, _a(f"模块 {num}"), align="C", new_x="LMARGIN", new_y="NEXT")
        self.set_font("CJK", "B", 22)
        self.set_text_color(*NAVY)
        self.cell(0, 12, _a(title), align="C", new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(*PRIMARY)
        mid = self.w / 2
        self.line(mid - 40, self.get_y() + 3, mid + 40, self.get_y() + 3)
        self.ln(12)

    def sec(self, title):
        self.ln(3)
        self.set_font("CJK", "B", 14)
        self.set_text_color(*NAVY)
        self.cell(0, 8, _a(title), new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(*BLUE)
        self.line(self.l_margin, self.get_y(), self.l_margin + 60, self.get_y())
        self.ln(4)

    def sub(self, title):
        self.set_font("CJK", "B", 11)
        self.set_text_color(*DARK)
        self.cell(0, 7, _a(title), new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def txt(self, text):
        self.set_font("CJK", "", 10)
        self.set_text_color(*TEXT)
        self.multi_cell(0, 5.5, _a(text), new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def bul(self, text):
        self.set_font("CJK", "", 10)
        self.set_text_color(*TEXT)
        self.multi_cell(0, 5.5, _a("  - " + text), new_x="LMARGIN", new_y="NEXT")

    def case_box(self, text):
        self.ln(2)
        x = self.l_margin
        w = self.w - self.l_margin - self.r_margin
        self.set_fill_color(*LIGHT_BG)
        self.set_draw_color(*BLUE)
        y_start = self.get_y()
        self.set_font("CJK", "B", 9)
        self.set_text_color(*PRIMARY)
        self.set_x(x + 3)
        self.cell(0, 5, _a("案例研究: ICU呼吸数字孪生"), new_x="LMARGIN", new_y="NEXT")
        self.set_font("CJK", "", 9.5)
        self.set_text_color(*DARK)
        self.set_x(x + 3)
        self.multi_cell(w - 6, 5, _a(text), new_x="LMARGIN", new_y="NEXT")
        y_end = self.get_y() + 2
        self.rect(x, y_start - 1, w, y_end - y_start + 2, style="DF")
        self.set_y(y_start)
        self.set_font("CJK", "B", 9)
        self.set_text_color(*PRIMARY)
        self.set_x(x + 3)
        self.cell(0, 5, _a("案例研究: ICU呼吸数字孪生"), new_x="LMARGIN", new_y="NEXT")
        self.set_font("CJK", "", 9.5)
        self.set_text_color(*DARK)
        self.set_x(x + 3)
        self.multi_cell(w - 6, 5, _a(text), new_x="LMARGIN", new_y="NEXT")
        self.ln(3)

    def exercise(self, text):
        self.ln(1)
        self.set_font("CJK", "B", 10)
        self.set_text_color(*GREEN)
        self.cell(30, 6, _a("练习:"), new_x="END", new_y="LAST")
        self.set_font("CJK", "", 10)
        self.set_text_color(*TEXT)
        self.multi_cell(0, 5.5, _a(text), new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def key_concept(self, text):
        self.set_font("CJK", "B", 10)
        self.set_text_color(*PRIMARY)
        self.cell(0, 6, _a("核心概念: " + text), new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def kv(self, key, val):
        self.set_font("CJK", "B", 10)
        self.set_text_color(*BLUE)
        self.cell(50, 5.5, _a(key), new_x="END", new_y="LAST")
        self.set_font("CJK", "", 10)
        self.set_text_color(*TEXT)
        self.multi_cell(0, 5.5, _a(val), new_x="LMARGIN", new_y="NEXT")


