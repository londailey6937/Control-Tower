"""
Generate the 510kBridge 5-Year Business Strategy PDF -- Chinese version.
510kBridge 五年商业策略 -- 中文版
"""

import os
from datetime import date
from fpdf import FPDF

OUT_DIR = os.path.dirname(os.path.abspath(__file__))

NAVY = (10, 40, 100)
TEXT = (40, 40, 45)
GRAY = (120, 120, 130)
WHITE = (255, 255, 255)
LIGHT_BG = (245, 247, 252)
GREEN = (20, 120, 60)
RED = (170, 40, 40)
ACCENT = (0, 100, 180)
GOLD = (160, 120, 20)


class StrategyPDF(FPDF):

    def __init__(self):
        super().__init__("P", "mm", "Letter")
        font_path = "/Library/Fonts/Arial Unicode.ttf"
        self.add_font("ARUNI", "", font_path, uni=True)
        self.add_font("ARUNI", "B", font_path, uni=True)
        self.add_font("ARUNI", "I", font_path, uni=True)

    def header(self):
        if self.page_no() == 1:
            return
        self.set_font("ARUNI", "I", 7)
        self.set_text_color(*GRAY)
        self.cell(0, 5, "机密文件  |  510kBridge 五年商业策略", align="R")
        self.ln(7)

    def footer(self):
        self.set_y(-13)
        self.set_font("ARUNI", "I", 7)
        self.set_text_color(*GRAY)
        self.cell(0, 5, f"第 {self.page_no()}/{{nb}} 页", align="C")

    def sec(self, title):
        if self.get_y() > self.h - 40:
            self.add_page()
        self.ln(3)
        self.set_font("ARUNI", "B", 13)
        self.set_text_color(*NAVY)
        self.cell(0, 8, title, new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(*NAVY)
        self.set_line_width(0.5)
        self.line(self.l_margin, self.get_y(),
                  self.w - self.r_margin, self.get_y())
        self.ln(4)

    def sub(self, title, color=NAVY):
        if self.get_y() > self.h - 30:
            self.add_page()
        self.set_font("ARUNI", "B", 11)
        self.set_text_color(*color)
        self.cell(0, 7, title, new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def sub2(self, title, color=ACCENT):
        self.set_font("ARUNI", "B", 10)
        self.set_text_color(*color)
        self.cell(0, 6, title, new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def txt(self, text):
        self.set_font("ARUNI", "", 10)
        self.set_text_color(*TEXT)
        self.multi_cell(0, 5.5, text, new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def green_txt(self, text):
        self.set_font("ARUNI", "B", 10)
        self.set_text_color(*GREEN)
        self.multi_cell(0, 5.5, text, new_x="LMARGIN", new_y="NEXT")
        self.set_text_color(*TEXT)
        self.ln(2)

    def gold_txt(self, text):
        self.set_font("ARUNI", "B", 10)
        self.set_text_color(*GOLD)
        self.multi_cell(0, 5.5, text, new_x="LMARGIN", new_y="NEXT")
        self.set_text_color(*TEXT)
        self.ln(2)

    def bullet(self, text, indent=6):
        self.set_font("ARUNI", "", 10)
        self.set_text_color(*TEXT)
        self.set_x(self.l_margin + indent)
        self.multi_cell(self.w - self.l_margin - self.r_margin - indent, 5.5,
                        f"- {text}", new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def bold_bullet(self, label, text, indent=6):
        self.set_x(self.l_margin + indent)
        self.set_font("ARUNI", "B", 10)
        self.set_text_color(*TEXT)
        lw = self.get_string_width(label) + 1
        self.cell(lw, 5.5, label)
        self.set_font("ARUNI", "", 10)
        self.multi_cell(0, 5.5, text, new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def table(self, headers, rows, col_w, bold_last=False):
        needed = 7 + len(rows) * 6 + 10
        if self.get_y() + needed > self.h - 20:
            self.add_page()
        self.set_font("ARUNI", "B", 8.5)
        self.set_text_color(*WHITE)
        self.set_fill_color(*NAVY)
        for i, h in enumerate(headers):
            self.cell(col_w[i], 7, h, border=1, fill=True, align="C")
        self.ln()
        self.set_text_color(*TEXT)
        for ri, row in enumerate(rows):
            is_last = bold_last and ri == len(rows) - 1
            self.set_font("ARUNI", "B" if is_last else "", 8.5)
            bg = (230, 235, 250) if is_last else (LIGHT_BG if ri % 2 == 0 else WHITE)
            self.set_fill_color(*bg)
            for ci, val in enumerate(row):
                align = "L" if ci == 0 else "C"
                self.cell(col_w[ci], 6, val, border=1, fill=True, align=align)
            self.ln()
        self.ln(3)

    def phase_banner(self, label, color):
        if self.get_y() > self.h - 50:
            self.add_page()
        self.ln(2)
        w = self.w - self.l_margin - self.r_margin
        self.set_fill_color(*color)
        y = self.get_y()
        self.rect(self.l_margin, y, w, 8, style="DF")
        self.set_xy(self.l_margin + 3, y + 1)
        self.set_font("ARUNI", "B", 11)
        self.set_text_color(*WHITE)
        self.cell(0, 6, label)
        self.set_y(y + 11)


